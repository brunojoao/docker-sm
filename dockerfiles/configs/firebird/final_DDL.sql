    EMAIL   VARCHAR(100),
    EMAIL2  VARCHAR(100),
    NRINSCR VARCHAR(20),
    NRCPF   VARCHAR(20),
    UFRG    VARCHAR(2),
    TPDOC   VARCHAR(10),
    OBSPOT  VARCHAR(255)
);

-- Creating the table FC07A00
CREATE TABLE FC07A00
(
    CDOBS   VARCHAR(50) NOT NULL,
