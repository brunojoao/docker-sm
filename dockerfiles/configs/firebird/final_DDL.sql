-- Creating the table FC04400
CREATE TABLE FC04400
(
    NRCRM  VARCHAR(10),
    UFCRM  VARCHAR(2),
    PFCRM  VARCHAR(2),
    NRTEL  VARCHAR(20),
    NRTEL2 VARCHAR(20),
    NRFAX  VARCHAR(20),
    PRIMARY KEY (NRCRM, UFCRM, PFCRM),
