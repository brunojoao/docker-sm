-- Creating the table FC15110
CREATE TABLE FC15110
(
    CDFIL        INTEGER,
    NRORC        INTEGER,
    SERIEO       VARCHAR(10),
    CDPRO        INTEGER,
    DESCR        VARCHAR(255),
    ITEMID       INTEGER,
    QUANT        NUMERIC(14, 2),
    UNIDA        VARCHAR(10),
