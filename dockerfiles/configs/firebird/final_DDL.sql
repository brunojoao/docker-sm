-- Creating the table FC99999
CREATE TABLE FC99999
(
    SUBARGUM  VARCHAR(50) NOT NULL,
    ARGUMENTO VARCHAR(50) NOT NULL,
    PARAMETRO VARCHAR(50),
    DESCRPAR  VARCHAR(100)
);

-- Creating the table FC07000
CREATE TABLE FC07000
(
    CDCLI   INTEGER PRIMARY KEY,
    NOMECLI VARCHAR(100),
    DTNAS   DATE,
