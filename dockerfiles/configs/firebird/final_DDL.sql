-- Creating the table FC08000
CREATE TABLE FC08000
(
    CDFUN   INTEGER PRIMARY KEY,
    NOMEFUN VARCHAR(100),
    USERID  VARCHAR(100)
);

-- Creating the table FC15000
CREATE TABLE FC15000
(
    CDFIL  INTEGER,
    NRORC  INTEGER,
    CDCLI  INTEGER,
    DTENTR DATE,
    NOMEPA VARCHAR(100),
    CDFUN  VARCHAR(10),
    VRRQU  NUMERIC(14, 2),
    VRDSC  NUMERIC(14, 2),
    PRIMARY KEY (CDFIL, NRORC),
    FOREIGN KEY (CDCLI) REFERENCES FC07000 (CDCLI)
);

-- Creating the table FC15100
CREATE TABLE FC15100
(
    CDFIL        INTEGER,
    NRORC        INTEGER,
    SERIEO       VARCHAR(10),
    PFCRM        VARCHAR(2),
    UFCRM        VARCHAR(2),
    NRCRM        VARCHAR(10),
    QTCONT       INTEGER,
    VRDSC        NUMERIC(14, 2),
    PRCOBR       NUMERIC(14, 2),
    PRREAL       NUMERIC(18, 2),
    CDCLI        INTEGER,
    DTENTR       DATE,
    CDFUNRE      INTEGER,
    CDFUNDG      INTEGER,
    NOMEPA       VARCHAR(100),
    VOLUME       NUMERIC(14, 2),
    TPFORMAFARMA VARCHAR(50),
    QTFOR        INTEGER,
    UNIVOL       VARCHAR(10),
    TITROT       VARCHAR(255),
    UNICONT      VARCHAR(10),
    TPCAP        VARCHAR(50),
    CDEMB        INTEGER,
    DTCAD        DATE,
    HRCAD        TIME,
    PRIMARY KEY (CDFIL, NRORC, SERIEO),
    FOREIGN KEY (CDFIL, NRORC) REFERENCES FC15000 (CDFIL, NRORC),
    FOREIGN KEY (CDCLI) REFERENCES FC07000 (CDCLI),
    FOREIGN KEY (CDFUNRE) REFERENCES FC08000 (CDFUN),
    FOREIGN KEY (CDFUNDG) REFERENCES FC08000 (CDFUN)
);

-- Creating the table FC03000
CREATE TABLE FC03000
(
    CDPRO        INTEGER PRIMARY KEY,
